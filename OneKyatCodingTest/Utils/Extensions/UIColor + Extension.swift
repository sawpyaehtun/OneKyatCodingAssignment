import UIKit

extension UIColor{
    //#5B9D2E
    class var primary_green : UIColor {
        return #colorLiteral(red: 0.3568627451, green: 0.6156862745, blue: 0.1803921569, alpha: 1)
    }
    
    //#F0F0F0
    class var background_gray : UIColor {
        return #colorLiteral(red: 0.9411764706, green: 0.9411764706, blue: 0.9411764706, alpha: 1)
    }
}
